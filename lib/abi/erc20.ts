// Map of chainId to deployed token addresses
const ERC20_ADDRESSES: Record<number, `0x${string}`> = {
  // Sepolia
  11155111: "0x847E870539E0dee6186DE97a229d66dC53511F0d",
  // Add other networks as needed
};

/**
 * Returns the ERC20 token address for the given chainId
 * @param chainId Chain ID to get the address for
 * @returns The token address as 0x-prefixed string
 */
export function getErc20Address(chainId: number): `0x${string}` {
  const address = ERC20_ADDRESSES[chainId];
  if (!address) {
    throw new Error(`No ERC20 token address configured for chainId ${chainId}`);
  }
  return address;
}
