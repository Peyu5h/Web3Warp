export function getEscrowAddress(chainId: number): `0x${string}` {
  const addresses: { [key: number]: `0x${string}` } = {
    11155111: "0x7331DeB1158389BE7bdA39CE2689fdEbA4Ec7fd3",
  };

  if (!addresses[chainId]) {
    throw new Error(`No escrow contract address for chainId ${chainId}`);
  }

  return addresses[chainId];
}

